package sec1;

public class Arith1 {

	public static void main(String[] args) {
		// ArithMatic1.
		//우선순위 : *, /, % > =+, -
		int a =20;
		int b =7;
		System.out.println("a%b="+(a%b)); //% 몫이 아닌 나머지
		System.out.println("홍홍홍은 "+"엔제리너스 "+1004+"근무자입니다.");
		// 정수+정수 => 정수
		//정수+실수 =>실수
		//실수 +실수 => 실수
		//문자열+숫자=>문자열
		//숫자+숫자+문자열 =>문자열 예시) 1+1+"홍홍" =>2홍홍

	}

}
